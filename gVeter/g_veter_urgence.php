        <p>En dehors de nos horaires d'ouverture, merci de contacter la Clinique vétérinaire d'Orléans des Dr Bedollo et Dr Benam au 02 38 00 00 22, située 8 rue de la Convalescence à Orléans.</p>
        <br>
        <p>Selon la nature de l’urgence, le régulateur Alvetis vous mettra soit en relation avec l’un de nos vétérinaires d’astreinte qui vous rejoindra à la Clinique vétérinaire d'Orléans, soit vous adressera un vétérinaire à domicile, soit vous indiquera les coordonnées d’un autre établissement d’urgence.</p>
